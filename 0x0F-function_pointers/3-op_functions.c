#include "3-calc.h"

/**
 * op_add - returns the addistion of two intgers
 * @a: first int
 * @b: second int
 * Return: int
 */

int op_add(int a, int b)
{
	return (a + b);
}




/**
 * op_sub - returns the sub of two intgers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}



/**
 * op_mul - returns the multiplication of two intgers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}




/**
 * op_div - returns the division of two intgers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}




/**
 * op_mod - returns the modulo of two intgers
 * @a: first int
 * @b: second int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}

